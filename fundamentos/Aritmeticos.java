package fundamentos;

public class Aritmeticos {
	public static void main(String[] args) {
		System.out.println(2 + 3);
		
		var x = 12.65;
		double y = 1.5;
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(x / y);
		
		Integer a = 5;
		int b = 3;
		
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println((double) a / b);
		
		System.out.println(a % b);
	}
}
